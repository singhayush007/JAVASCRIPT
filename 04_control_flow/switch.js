// switch 


const month = "march" 

switch (month) {
    case "jan":
        consolelog("Jan");
        break;
     case "feb" :
        console.log("feb");
        break;
        case "march":
            console.log("March");
            break;
            case "april":
            console.log("april");
            break;
    default:
        console.log("default case match");
        break;
}